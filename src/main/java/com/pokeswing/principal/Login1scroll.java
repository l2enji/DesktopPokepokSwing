    private void btnLoginConnectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginConnectionActionPerformed
        if(txtIdentifiant.getText().equals("") || txtMotdepasse.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Entrer les donnÃ©es dans les champs","Message",JOptionPane.INFORMATION_MESSAGE);
        } else{
            try {
                listLogin = udao.rechercherLogin(txtIdentifiant.getText(), DataEncryption.getSHA1(txtMotdepasse.getText()));
                //listLogin = udao.rechercherLogin(txtIdentifiant.getText(), txtMotdepasse.getText());//Cherche dans la liste de login la methode rechercherLogin dans la classe "UserDao"
                //code sans cryptage du mot de passe
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(Login1scroll.class.getName()).log(Level.SEVERE, null, ex);
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(Login1scroll.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        if(listLogin.size()>=1){
            if(listLogin.get(0).getAuthorization().equalsIgnoreCase("administrateur")){

                PokeDesktopAdmin desktopAdmin = new PokeDesktopAdmin();
                desktopAdmin.setVisible(true); //pour rendre visibla la page admin
                desktopAdmin.afficherUser(txtIdentifiant.getText());
                dispose(); //methode pour vider la memoire

            } else {
                PokeDesktopUtilisateur desktopClient = new PokeDesktopUtilisateur();
                desktopClient.setVisible(true);
                desktopClient.afficherUser(txtIdentifiant.getText());
                dispose();
            }
        }
    }//GEN-LAST:event_btnLoginConnectionActionPerformed

    
    
    
    
    
