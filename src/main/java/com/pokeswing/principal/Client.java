    private void btnModifierCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierCActionPerformed
        
        try {
            DefaultTableModel dtm = (DefaultTableModel) tabCLIENT.getModel();
            int selectedIndex = tabCLIENT.getSelectedRow();
            
            int idClient = Integer.parseInt(dtm.getValueAt(selectedIndex, 0).toString()); // integer.parseInt=> converti le type en entier
            String nom = txtNOM.getText();
            String prenom = txtPRENOM.getText();
            String adresse = txtADRESSE.getText();
            String codepostal = txtCODEPOSTAL.getText();
            String ville = txtVILLE.getText();
            String pays = txtPAYS.getText();
            String telephone = txtTELEPHONE.getText();
            String email = txtEMAIL.getText();
            
            
            Class.forName("com.mysql.cj.jdbc.Driver"); //Enregistre le driver
            con = DriverManager.getConnection("jdbc:mysql://localhost/pokedb", "root","prout_KK1987");
            pst = con.prepareStatement("update clients set NOM=?, PRENOM=?, ADRESSE=?, CODEPOSTAL=?, VILLE=?, PAYS=?, TELEPHONE=?, EMAIL=? where IDCLIENT=?");
            pst.setString(1, nom);
            pst.setString(2, prenom);
            pst.setString(3, adresse);
            pst.setString(4, codepostal);
            pst.setString(5, ville);
            pst.setString(6, pays);
            pst.setString(7, telephone);
            pst.setString(8, email);
            pst.setInt(9, idClient); // attention Int
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Données mise à jour");
            
            txtNOM.setText("");
            txtPRENOM.setText("");
            txtADRESSE.setText("");
            txtCODEPOSTAL.setText("");
            txtVILLE.setText("");
            txtPAYS.setText("");
            txtTELEPHONE.setText("");
            txtEMAIL.setText("");
            
            tableUpdate();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PokeDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PokeDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
    }//GEN-LAST:event_btnModifierCActionPerformed

