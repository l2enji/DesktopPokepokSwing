    private void btnSupprimerCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerCActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) tabCLIENT.getModel();
        int selectedIndex = tabCLIENT.getSelectedRow();
        int idClient = Integer.parseInt(dtm.getValueAt(selectedIndex, 0).toString());
        int dialogResult = JOptionPane.showConfirmDialog(null, "Voulez-vous supprimer cette données?","Attention",JOptionPane.YES_NO_OPTION);
        
        if(dialogResult == JOptionPane.YES_OPTION ){
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/pokedb", "root","prout_KK1987");
                pst = con.prepareStatement("delete from clients where IDCLIENT=?");
                pst.setInt(1, idClient);
                
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(this,"Les données ont été supprimées");
                
                txtNOM.setText("");
                txtPRENOM.setText("");
                txtADRESSE.setText("");
                txtCODEPOSTAL.setText("");
                txtVILLE.setText("");
                txtPAYS.setText("");
                txtTELEPHONE.setText("");
                txtEMAIL.setText("");
                
                tableUpdate();
            
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(PokeDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(PokeDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }
    }//GEN-LAST:event_btnSupprimerCActionPerformed

    
