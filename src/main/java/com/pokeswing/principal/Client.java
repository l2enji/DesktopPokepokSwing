    private void tableUpdate(){
            int cpt;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
                    con = DriverManager.getConnection("jdbc:mysql://localhost/pokedb", "root","prout_KK1987"); // Demande au DriverManager (gestionnaire de manager) de récupérer et de stocker les infos dans la variable con
                    pst = con.prepareStatement("SELECT * FROM clients");
                    ResultSet rs = pst.executeQuery();
                    ResultSetMetaData rsmd = rs.getMetaData();
                    cpt = rsmd.getColumnCount();
                    DefaultTableModel dtm = (DefaultTableModel) tabCLIENT.getModel();
                    dtm.setRowCount(0);
                    
                    while(rs.next()){
                        Vector  vect = new Vector();
                        
                        for(int i = 1; i <= cpt; i++){
                            vect.add(rs.getString("IDCLIENT"));
                            vect.add(rs.getString("NOM"));
                            vect.add(rs.getString("PRENOM"));
                            vect.add(rs.getString("ADRESSE"));
                            vect.add(rs.getString("CODEPOSTAL"));
                            vect.add(rs.getString("VILLE"));
                            vect.add(rs.getString("PAYS"));
                            vect.add(rs.getString("TELEPHONE"));
                            vect.add(rs.getString("EMAIL"));
                            
                        }
                    dtm.addRow(vect);
                    }
                            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PokeDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PokeDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } 
        }
    
    private void btnAjouterCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterCActionPerformed
        // Ajout fournisseur via le bouton "ajouter"
        String nom = txtNOM.getText();
        String prenom = txtPRENOM.getText();
        String adresse = txtADRESSE.getText();
        String codePostal = txtCODEPOSTAL.getText();
        String ville = txtVILLE.getText();
        String pays = txtPAYS.getText();
        String telephone = txtTELEPHONE.getText();
        String email = txtEMAIL.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/pokedb", "root","prout_KK1987"); // Demande au DriverManager (gestionnaire de manager) de récupérer et de stocker les informations dans la variable con
            pst = con.prepareStatement("INSERT INTO clients(NOM,PRENOM,ADRESSE,CODEPOSTAL,VILLE,PAYS,TELEPHONE,EMAIL) VALUES(?,?,?,?,?,?,?,?)"); //appel du "statement" contenant toutes les informations
            
            pst.setString(1, nom);
            pst.setString(2, prenom);
            pst.setString(3, adresse);
            pst.setString(4, codePostal);
            pst.setString(5, ville);
            pst.setString(6, pays);
            pst.setString(7, telephone);
            pst.setString(8, email);
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Données enregistrées"); // pour afficher une fenetre
            
            //Pour vider le contenu des champs après avoir ajouter un objet
            txtNOM.setText("");
            txtPRENOM.setText("");
            txtADRESSE.setText("");
            txtCODEPOSTAL.setText("");
            txtVILLE.setText("");
            txtPAYS.setText("");
            txtTELEPHONE.setText("");
            txtEMAIL.setText("");
            
            tableUpdate();
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PokeDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PokeDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAjouterCActionPerformed

